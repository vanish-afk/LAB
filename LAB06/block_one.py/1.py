import math

# Введите три числа
a = float(input("Введите первое число: "))
b = float(input("Введите второе число: "))
c = float(input("Введите третье число: "))

# Найти максимальное число
max_num = max(a, b, c)

# Вычислить синус максимального числа
sin_max = math.sin(max_num)

# Вывести результат
print(f"Синус максимального числа ({max_num}) равен: {sin_max}")