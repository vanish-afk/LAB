import math
n = int(input("Введите число n: "))
current = 1  # Первое число последовательности
step = 2     # Разница между текущим и следующим числом

while current <= n:  # Продолжаем, пока число не превысит n
    current += step  # Увеличиваем число на текущий шаг
    step += 1        # Увеличиваем шаг (по формуле последовательности)

print(f"Первое число из последовательности, большее {n}: {current}")
